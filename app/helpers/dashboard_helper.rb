  # Navigation helpers for sidebar
  def nav_active?(path_pattern)
    case path_pattern
    when "/settings"
      request.path.match?(%r{/projects/[^/]+/edit$})
    when "/overview"
      request.path.match?(%r{/projects/[^/]+/overview$})
    when "/analytics"
      request.path.match?(%r{/projects/[^/]+/analytics$})
    when "/setup"
      request.path.match?(%r{/projects/[^/]+/setup$})
    else
      request.path.include?(path_pattern)
    end
  end

  def nav_link_class(path_pattern)
    nav_active?(path_pattern) ? "nav-item active" : "nav-item"
  end

  def format_duration(seconds)
    return "â€”" unless seconds

    if seconds < 60
      "#{seconds}s"
    elsif seconds < 3600
      "#{(seconds / 60).round}m"
    elsif seconds < 86400
      hours = seconds / 3600
      mins = (seconds % 3600) / 60
      mins > 0 ? "#{hours.round}h #{mins.round}m" : "#{hours.round}h"
    else
      days = seconds / 86400
      hours = (seconds % 86400) / 3600
      hours > 0 ? "#{days.round}d #{hours.round}h" : "#{days.round}d"
    end
  end
